								<h3>Les débuts de l'homme araignée</h3>
								<p><strong>Peter Parker</strong>, alias <strong>Spider-Man</strong> est un <strong>super-héro</strong> évoluant dans <a href="https://fr.wikipedia.org/wiki/Univers_Marvel">l'univers <strong>Marvel</strong></a> de la maison d'édition <strong>Marvel Comics</strong>.
									Imaginé par le scénariste <a href="https://fr.wikipedia.org/wiki/Stan_Lee"><strong>Stan Lee</strong></a> et le dessinateur <a href="https://fr.wikipedia.org/wiki/Steve_Ditko"><strong>Steve Ditko</strong></a>, il apparaît pour la première fois dans le <strong>comic book Amazing Fantasy</strong> (vol. 1) #15 d’août 1962.</p>
								<p>Ses parents sont tués en travaillant sous couverture pour le gouvernement . 
									Orphelin à l'âge de six ans, <strong>Peter</strong> est alors confié aux soins de sa tante et son oncle, May et <a href="https://fr.wikipedia.org/wiki/Benjamin_Parker">Benjamin Parker</a> qu’il surnome <a href="https://fr.wikipedia.org/wiki/Benjamin_Parker"><strong>Oncle Ben</strong></a> et qui sera une figure parentale importante pour lui.
									Il étudie au lycée de Midtown, dans le <a href="https://fr.wikipedia.org/wiki/Queens">Queens</a> à New York.
									Peu populaire mais studieux, surtout en sciences. 
									Il a le béguin pour <a hef="https://fr.wikipedia.org/wiki/Liz_Allen">Liz Allen</a> qui, elle, aime <a href="https://fr.wikipedia.org/wiki/Flash_Thompson">Flash Thompson</a>.
									Il obtient ses pouvoirs à la suite d'une expérience à laquelle il assiste, il se fait <strong>mordre par une araignée radioactive</strong>. 
									Cette morsure lui confère les <strong>super-pouvoirs</strong> suivants : Force et agilité surhumaine, l’adhésion aux parois et un 6e sens détectant les dangers imminents.

									Il va s’en servir pour gagner de l’argent sans faire de méfait pour aider son <a href="https://fr.wikipedia.org/wiki/Benjamin_Parker">oncle</a> et sa tante ,mais va décider de lutter contre la criminalitée en suivant l’adage <strong>“De grands pouvoirs impliquent de grandes responsabilités”</strong>.
 
									Il essaye d’intégrer les <a href="https://fr.wikipedia.org/wiki/Quatre_Fantastiques">Quatre Fantastiques</a> mais il échoue et comprend qu’ils ne sont même pas payés pour leur actions.

									Il va se servir de ses <strong>pouvoirs</strong> et de son appareil photo pour vendre des clichés aux journaux et être embauché par le Daily Bugle, un journal New Yorkais.

									<strong>Peter Parker</strong> se retrouve dans une situation compliqué où il doit vivre sa vie d’adolescent et caché sa double identité qui risque d’être révélé à bien des moments. 

									Il va par la suite affronter de multiples <a href="https://fr.wikipedia.org/wiki/Super-vilain"><strong>super-vilains</strong></a> : le <a href="https://fr.wikipedia.org/wiki/Docteur_Octopus"><strong>Docteur Octopus</strong></a>, l'<a href="https://fr.wikipedia.org/wiki/Homme-sable"><strong>Homme-Sable</strong></a>, <a href= "https://fr.wikipedia.org/wiki/Myst%C3%A9rio"><strong>Mystério</strong></a>, le  <a href="https://fr.wikipedia.org/wiki/Bouffon_vert"><strong>Bouffon Vert</strong></a> et <a href= "https://fr.wikipedia.org/wiki/Kraven_le_chasseur_(Serguei_Kravinoff)"><strong>Kraven</strong></a>.
									
									Il va vivre deux histoire d’amour , avec <a href="https://fr.wikipedia.org/wiki/Mary_Jane_Watson"><strong>Mary Jane Watson</strong></a> d’abord puis avec <a href="https://fr.wikipedia.org/wiki/Gwen_Stacy">Gwen Stacy</a> plus tard , il retournera vers <a href="https://fr.wikipedia.org/wiki/Mary_Jane_Watson"><strong>Mary Jane Watson</strong></a> après la mort tragique de <a href="https://fr.wikipedia.org/wiki/Gwen_Stacy">Gwen Stacy</a> qui le hantera longtemps (il s’agit d’un des grand événement symbolique de la fin de <a href= "https://fr.wikipedia.org/wiki/%C3%82ge_d%27argent_des_comics">l'âge d'argent des comics</a>).

